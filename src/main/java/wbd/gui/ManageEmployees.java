    private void refreshList() {
        dlm.removeAllElements();
        Connection con = MainWindow.getInstance().getConnection();
        Statement stmt = null;
        String query = "SELECT id_pracownika, imie, nazwisko FROM pracownicy";

        try {
            try {
                stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery(query);
                while (rs.next()) {
                    int id = rs.getInt("id_pracownika");
                    String name = rs.getString("imie");
                    String surname = rs.getString("nazwisko");
                    Employee employee = new Employee(id, name, surname);
                    dlm.addElement(employee);
                }
            } finally {
                if (stmt != null) {
                    stmt.close();
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

 
