    private int setEmployeeID(){
                int employeeID=0;
        
        Connection con = MainWindow.getInstance().getConnection();
        Statement stmt = null;
        String query = 
                "SELECT max(id_pracownika) FROM pracownicy";
        try {
            try {
                stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery(query);
              if(rs.next())
              employeeID=rs.getInt("max(id_pracownika)");
                }
            finally {
                if (stmt != null) {
                    stmt.close();
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return employeeID+1;

    }
