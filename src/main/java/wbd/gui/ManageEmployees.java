    private class Employee{
        int id;
        String name;
        String surname;
        String birthDate;
        String telNumber;
        String email;
        String contractType;
        

        public Employee(int id, String name, String surname) {
            this.id = id;
            this.name = name;
            this.surname = surname;
        }

        @Override
        public String toString() {
            return String.format("%03d %20s %20s" , id, name, surname);
        }
        public int getID(){
            return id;
        }
    }
    
