    private void refreshList() {
        dlmAnimal.removeAllElements();
        dlmCaretaker.removeAllElements();
        Connection con = MainWindow.getInstance().getConnection();
        Statement stmt1 = null;
        Statement stmt2 = null;
        String query1 = "SELECT id_zwierzaka, imie, rodzaj FROM zwierzaki";
        String query2 = "SELECT id_opiekuna, imie, nazwisko FROM opiekunowie";

        try {
            try {
                stmt1 = con.createStatement();
                ResultSet rs1 = stmt1.executeQuery(query1);
                while (rs1.next()) {
                    int id = rs1.getInt("id_zwierzaka");
                    String name = rs1.getString("imie");
                    String kind = rs1.getString("rodzaj");
                    Animal animal = new Animal(id, name, kind);
                    dlmAnimal.addElement(animal);
                }
                stmt2 = con.createStatement();
                ResultSet rs2 = stmt2.executeQuery(query2);
                while (rs2.next()) {
                    int id = rs2.getInt("id_opiekuna");
                    String name = rs2.getString("imie");
                    String surname = rs2.getString("nazwisko");
                    Caretaker caretaker = new Caretaker(id, name, surname);
                    dlmCaretaker.addElement(caretaker);
                }
            } finally {
                if (stmt1 != null && stmt2 != null) {
                    stmt1.close();
                    stmt2.close();
                }
            }
                
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

