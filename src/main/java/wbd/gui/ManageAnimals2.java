    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        if(evt.getValueIsAdjusting())
            return;
        
        Animal selectedAnimal = (Animal)dlmAnimal.getElementAt(jList1.getSelectedIndex());
        System.out.println(selectedAnimal);
         int animalID=selectedAnimal.getID();
         jTextField11.setText(selectedAnimal.getName());
        Connection con = MainWindow.getInstance().getConnection();
        Statement stmt = null;
        String query = 
                "SELECT imie, zwierzaki.rodzaj, rok_urodzenia, rasa, opis_rasy, max(`data`) as 'ostatnie_badanie' "
                + "from zwierzaki "
                + "join rasy_relacja using (ID_Rasy)" 
                + "join badania using (ID_Zwierzaka) where ID_zwierzaka = " 
                + Integer.toString(selectedAnimal.id);
        try {
            try {
                stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery(query);
                while (rs.next()) {
                    String name = rs.getString("imie");
                    String kind = rs.getString("zwierzaki.rodzaj");
                    String race = rs.getString("rasa");
                    String birthYear = rs.getString("rok_urodzenia");
                    String raceDescription = rs.getString("opis_rasy");
                    String lastTest = rs.getString("ostatnie_badanie");
                    
                    AnimalDetails details = new AnimalDetails(selectedAnimal.id, name, kind, race, birthYear, raceDescription, lastTest);
                    fillDetails(details);
                }
            } finally {
                if (stmt != null) {
                    stmt.close();
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_jList1ValueChanged
 
