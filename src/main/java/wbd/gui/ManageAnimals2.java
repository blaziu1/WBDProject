    private int setAdoptionID(){
        int adoptionID=0;
        
        Connection con = MainWindow.getInstance().getConnection();
        Statement stmt = null;
        String query = 
                "SELECT max(id_adopcji) FROM adopcje";
        try {
            try {
                stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery(query);
              if(rs.next())
              adoptionID=rs.getInt("max(id_adopcji)");
                }
            finally {
                if (stmt != null) {
                    stmt.close();
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return adoptionID+1;
    }
    
