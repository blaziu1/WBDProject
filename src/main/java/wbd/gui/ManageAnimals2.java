    private class Animal{
        int id;
        String name;
        String kind;
        String year;

        public Animal(int id, String name, String kind) {
            this.id = id;
            this.name = name;
            this.kind = kind;
        }

        @Override
        public String toString() {
            return String.format("%03d %20s %20s" , id, name, kind);
        }
        
        public int getID(){
            return id;
        }
        
        public String getName(){
            return name;
        }
        
        public String getKind(){
            return kind;
        }
        
        public String getYear(){
            return year;
        }
    }
    
